package cn.mycloudway.demo;

public class ArithmeticOperatorDemo5 {
    public static void main(String[] args) {
        int a = 10;

        a++;
        System.out.println(a);

        ++a;
        System.out.println(a);

        a--;
        System.out.println(a);

        --a;
        System.out.println(a);
    }
}
